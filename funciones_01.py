def sumar(num1,num2):
    resultado=num1+num2
    return resultado

def restar(num1,num2):
    resultado=num1-num2
    return resultado

def multiplicar(num1,num2):
    resultado=num1*num2
    return resultado

def dividir(num1,num2):
    if(num2>0):
        resultado=num1/num2
    else:
        resultado='No se puede'
    return resultado


